// ─────────────────────────────────────────
// СИСТЕМА ВОСПРОИЗВЕДЕНИЯ РЕПЛИК
// ─────────────────────────────────────────

const NPCSystem = {
  timers: {},

  // Инициализация NPC на экране здания
  init(buildingId) {
    const npc = NPC_DATA[buildingId];
    if (!npc) return;

    this.render(npc);
    this.startIdleRotation(buildingId);
    this.say(buildingId, 'idle'); // первая реплика при входе
  },

  // Отрисовка NPC
  render(npc) {
    const container = document.getElementById(`npc-container-${npc.id}`);
    if (!container) return;

    container.innerHTML = `
      <div class="npc-wrap npc-${npc.position}">
        <div class="npc-bubble-wrap ${npc.bubbleDirection === 'left' ? 'bubble-left' : 'bubble-right'}">
          <div class="npc-bubble" id="npc-bubble-${npc.id}"></div>
        </div>
        <img src="${npc.image}" class="npc-img" alt="${npc.name}"
             onerror="this.style.display='none'">
      </div>`;
  },

  // Произнести случайную реплику из категории
  say(buildingId, category, subcategory = null) {
    const npc = NPC_DATA[buildingId];
    if (!npc) return;

    let phrases;
    if (category === 'idle') {
      phrases = npc.idle;
    } else if (category === 'trigger' && subcategory) {
      phrases = npc.triggers[subcategory];
    }
    if (!phrases || !phrases.length) return;

    const text = phrases[Math.floor(Math.random() * phrases.length)];
    const bubble = document.getElementById(`npc-bubble-${buildingId}`);
    if (!bubble) return;

    // Анимация смены реплики
    bubble.style.opacity = '0';
    setTimeout(() => {
      bubble.textContent = `"${text}"`;
      bubble.style.opacity = '1';
    }, 200);
  },

  // Триггер события
  trigger(buildingId, eventKey) {
    this.say(buildingId, 'trigger', eventKey);
    // После триггерной реплики — возврат к idle через 5 секунд
    clearTimeout(this.timers[`${buildingId}_trigger`]);
    this.timers[`${buildingId}_trigger`] = setTimeout(() => {
      this.say(buildingId, 'idle');
    }, 5000);
  },

  // Автоматическая смена idle реплик
  startIdleRotation(buildingId) {
    this.stopIdleRotation(buildingId);
    this.timers[`${buildingId}_idle`] = setInterval(() => {
      // Не перебивать триггерную реплику
      if (!this.timers[`${buildingId}_trigger`]) {
        this.say(buildingId, 'idle');
      }
    }, 45000); // каждые 45 секунд
  },

  stopIdleRotation(buildingId) {
    clearInterval(this.timers[`${buildingId}_idle`]);
    delete this.timers[`${buildingId}_idle`];
  },

  // Очистка при выходе из здания
  cleanup(buildingId) {
    this.stopIdleRotation(buildingId);
    clearTimeout(this.timers[`${buildingId}_trigger`]);
  },
};

// Экспорт
if (typeof module !== 'undefined') {
  module.exports = { NPC_DATA, NPCSystem };
}
